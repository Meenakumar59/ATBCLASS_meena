package May.EX_04052024;

public class Lab_94 {
//    triangle pattern
public static void main(String[] args) {
    int n = 3;//matrices
    for(int i =1; i<=n; i++){
        for(int j =1; j<=i; j++){
            System.out.print("*");
        }
        System.out.println("");// i=1, j=1 ==> j max valuse is i
    }

}
}
