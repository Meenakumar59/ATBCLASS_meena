package EX_13042024;

public class Lab09_datatypes {
    public static void main(String[] args) {
        int age = 25;
//        %d = any integer - int , byte , double and long
//        %c - char
//        %cf- float
//        %s - string
        System.out.printf("Meena age is %d" ,age);
        System.out.println();
        System.out.println("Meena age is " +  age);
        float pi = 3.14f;
        System.out.printf("pi value is %f", pi);
        String name = "meena";
        System.out.printf("your name is -> %s" , name);
        System.out.println();
        System.out.println("ypur name is -->" + name);

    }
}
